function DatosArreglos(){
    let Datos=[3,7,9,3,2,6]
    let suma=0
    for(i=0;i<Datos.length;i++){
        if(Datos[i]!=0){
            suma+=Datos[i]
        }
    } console.log(`la suma de numeros impares es ${suma}`)
} DatosArreglos()

